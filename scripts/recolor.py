def rgb_sub(m):
    r, g, b = int(m.group('r')), int(m.group('g')), int(m.group('b'))
    rest = m.group('rest')
    if (r, g, b) in rgb_map:
        nr, ng, nb = rgb_map[(r, g, b)]
        return f"{m.group('fn')}({nr}, {ng}, {nb}{rest}"
    return m.group(0)

